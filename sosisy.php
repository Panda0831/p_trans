  <script>
    document.addEventListener('DOMContentLoaded', function() {
      const carousel = document.querySelector('.club-cards');
      const cards = document.querySelectorAll('.club-card');
      const prevBtn = document.querySelector('.prev');
      const nextBtn = document.querySelector('.next');
      
      let currentIndex = 0;
      const cardWidth = cards[0].offsetWidth + 30; // width + gap
      const visibleCards = Math.floor(document.querySelector('.carousel-container').offsetWidth / cardWidth);
      
      // Position initiale
      updateCarousel();
      
      // Événements des boutons
      prevBtn.addEventListener('click', function() {
        if (currentIndex > 0) {
          currentIndex--;
          updateCarousel();
        }
      });
      
      nextBtn.addEventListener('click', function() {
        if (currentIndex < cards.length - visibleCards) {
          currentIndex++;
          updateCarousel();
        }
      });
      
      function updateCarousel() {
        const transformValue = -currentIndex * cardWidth;
        carousel.style.transform = `translateX(${transformValue}px)`;
        
        // Désactiver les boutons si nécessaire
        prevBtn.disabled = currentIndex === 0;
        nextBtn.disabled = currentIndex >= cards.length - visibleCards;
        
        // Style des boutons désactivés
        if (prevBtn.disabled) {
          prevBtn.style.opacity = '0.5';
          prevBtn.style.cursor = 'not-allowed';
        } else {
          prevBtn.style.opacity = '1';
          prevBtn.style.cursor = 'pointer';
        }
        
        if (nextBtn.disabled) {
          nextBtn.style.opacity = '0.5';
          nextBtn.style.cursor = 'not-allowed';
        } else {
          nextBtn.style.opacity = '1';
          nextBtn.style.cursor = 'pointer';
        }
      }
      
      // Gestion du redimensionnement
      window.addEventListener('resize', function() {
        const newVisibleCards = Math.floor(document.querySelector('.carousel-container').offsetWidth / cardWidth);
        if (currentIndex > cards.length - newVisibleCards) {
          currentIndex = Math.max(0, cards.length - newVisibleCards);
          updateCarousel();
        }
      });
    });
  </script>
